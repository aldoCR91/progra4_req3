

class Botella:
    
    # Constructor de la clase
    def __init__(self, capacidad: float, color: str, dimensiones: dict) -> None:
        self.capacidad = capacidad
        self.color = color
        self.dimensiones = dimensiones
        
    def set_capacidad(self, capacidad: float) -> None:
        self.capacidad = capacidad
        
    def set_color(self, capacidad: float) -> None:
        self.capacidad = capacidad
        
    def set_dimensiones(self, capacidad: float) -> None:
        self.capacidad = capacidad
    
    def get_capacidad(self):
        return self.capacidad
    
    def get_color(self):
        return self.color
    
    def get_dimensiones(self):
        return self.dimensiones

